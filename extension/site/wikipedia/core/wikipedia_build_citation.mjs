import { simpleBuildCitationWrapper } from "../../../base/core/citation_builder.mjs";

function buildWikipediaUrl(ed, builder) {
  if (ed.permalink) {
    return ed.permalink;
  }
  return ed.url;
}

function buildSourceTitle(ed, gd, builder) {
  builder.sourceTitle = ed.title;
}

function buildSourceReference(ed, gd, builder) {
  builder.sourceReference = "Wikipedia, The Free Encyclopedia, Wikipedia contributors";
}

function buildRecordLink(ed, gd, builder) {
  var wikipediaUrl = buildWikipediaUrl(ed, builder);

  let recordLink = "[" + wikipediaUrl + " Wikipedia]";
  builder.recordLinkOrTemplate = recordLink;
}

function buildCoreCitation(ed, gd, builder) {
  // Example Chicago citation generated by WikiPedia:
  // Wikipedia contributors, "World War I," Wikipedia, The Free Encyclopedia, https://en.wikipedia.org/w/index.php?title=World_War_I&oldid=1180666851 (accessed October 20, 2023).
  buildSourceTitle(ed, gd, builder);
  buildSourceReference(ed, gd, builder);
  buildRecordLink(ed, gd, builder);
}

function buildCitation(input) {
  return simpleBuildCitationWrapper(input, buildCoreCitation);
}

export { buildCitation };
